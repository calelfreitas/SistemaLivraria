    // Botão Salvar - Refatorado por Manus
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String strTitulo = titulo.getText();
        String strAutor = autor.getText();
        String strCategoria = assunto.getText(); // Usando o campo assunto como categoria
        String strIsbn = isbn.getText();
        String strEstoque = estoque.getText();
        String strPreco = preco.getText();
        Editora editoraSelecionada = (Editora) editora.getSelectedItem();

        // Validações
        if (strTitulo.trim().isEmpty() ||
            strAutor.trim().isEmpty() ||
            strCategoria.trim().isEmpty() ||
            strIsbn.trim().isEmpty() ||
            strEstoque.trim().isEmpty() ||
            strPreco.trim().isEmpty() ||
            editoraSelecionada == null) {
            JOptionPane.showMessageDialog(this, "Erro, preencha todos os campos e selecione uma editora!", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int valEstoque;
        double valPreco;
        try {
            valEstoque = Integer.parseInt(strEstoque);
            if (valEstoque < 0) throw new NumberFormatException("Estoque não pode ser negativo.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro no campo Estoque: " + e.getMessage(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            valPreco = Double.parseDouble(strPreco.replace(",", ".")); // Aceita vírgula como separador decimal
            if (valPreco < 0) throw new NumberFormatException("Preço não pode ser negativo.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro no campo Preço: " + e.getMessage(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Cria o objeto Livro com os dados da tela
            Livro novoLivro = new Livro(strTitulo, strAutor, strIsbn, editoraSelecionada, valEstoque, valPreco, strCategoria);

            // Usa o controle para cadastrar, passando o objeto
            livroControle.cadastrarLivro(novoLivro);

            JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso! (ID: " + novoLivro.getId() + ")", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
            // dispose(); // Opcional
        } catch (SQLException | IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar livro:\n" + ex.getMessage(), "Erro no Cadastro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void limparCampos(){
        titulo.setText("");
        autor.setText("");
        assunto.setText("");
        isbn.setText("");
        estoque.setText("");
        preco.setText("");
        editora.setSelectedIndex(-1); // Limpa seleção da editora
        titulo.requestFocus();
    }

