    // Método para carregar editoras no ComboBox
    private void carregarEditoras() {
        try {
            List<Editora> listaEditoras = editoraControle.listarTodasEditoras();
            // Usar Vector para DefaultComboBoxModel
            Vector<Editora> editorasVector = new Vector<>(listaEditoras);
            DefaultComboBoxModel<Editora> model = new DefaultComboBoxModel<>(editorasVector);
            editora.setModel(model);
            editora.setSelectedIndex(-1); // Nenhum item selecionado inicialmente
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar editoras: " + e.getMessage(), "Erro Banco de Dados", JOptionPane.ERROR_MESSAGE);
            // Tratar erro - talvez desabilitar o formulário ou fechar
        }
    }

